package pl.sportcode.operatorsVariablesComment;

public class Task1 {
  public static void main(String[] args) {
	  int numberFirst = 15 ;
	  int numberSecond = 5 ;
	  int numberFor , numberFive , numberSix , numberSeven , numberEight ;
	  
	  numberFor = numberFirst + numberSecond ;
	  numberFive = numberFirst - numberSecond ;
	  numberSix = numberFirst % numberSecond ;
	  numberSeven = numberFirst / numberSecond ;
	  numberEight = numberFirst * numberSecond ;
	  
	  System.out.println("numberFor = "+numberFor);
	  System.out.println("numberFive = "+numberFive);
	  System.out.println("numberSix = "+numberSix);
	  System.out.println("numberSeven = "+numberSeven);
	  System.out.println("numberEight = "+numberEight);
  }
}
